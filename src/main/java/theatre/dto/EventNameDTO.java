package theatre.dto;

public class EventNameDTO {

    private long id;
    private String title;

    public EventNameDTO() {}

    public long getId() {
        return id;
    }
    public void setId(long id) {
        this.id = id;
    }
    public String getTitle() {
        return title;
    }
    public void setTitle(String title) {
        this.title = title;
    }
}